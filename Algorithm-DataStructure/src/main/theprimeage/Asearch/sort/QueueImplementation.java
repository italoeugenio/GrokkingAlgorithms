package main.theprimeage.Asearch.sort;

class Qnode{
    int data;
    Qnode next;

    public Qnode(int data, Qnode next) {
        this.data = data;
        this.next = null;
    }
}


public class QueueImplementation {
    public int length;
    private Qnode head;
    private Qnode tail;

    public QueueImplementation() {
        this.length = length;
        this.head = head;
        this.tail = tail;
    }
}
